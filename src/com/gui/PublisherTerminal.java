private class TableControler extends Thread {
        Thread tbThread = null;
        int size = 0;
        public TableControler(){
            tbThread = this;
        }
        List<Publisher> temp;
        @Override
        public void run() {
            boolean status = true;
            size = 0;
            while (true) {
                temp = user.getPubList();
                temp.sort(new Publisher());
                if (temp != null) {
                    if (size < temp.size()) {
                        for (int i = size; i < temp.size(); i++) {
                            Publisher t = temp.get(i);
                            String[] rowData = {t.getpID(), t.getName(), t.getPhoneNumber()};
                            DefaultTableModel tm = (DefaultTableModel) pubtable.getModel();
                            tm.addRow(rowData);
                        }
                        size = temp.size();
                    }
                }
                try {
                    Thread.sleep(100);
                } catch (InterruptedException ex) {
                    Logger.getLogger(PublisherTerminal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        public void stopTable(){
            this.yield();
        }
        @Override
        public void start(){
            tbThread = new Thread(this);
            tbThread.start();
        }
        
        public void resetTable(int mode){
            DefaultTableModel tm = (DefaultTableModel) pubtable.getModel();
            tm.setRowCount(0);
            size = 0;
            if(mode == 1 && !this.isAlive()) this.start();
        }
    }
