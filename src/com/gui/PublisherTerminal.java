    private void IDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_IDFocusLost
        // TODO add your handling code here:
        if(ID.getText().isEmpty()) messageSystem("PID is empty, please check again", pidmess);
        else{
            if(!ID.getText().matches("P[\\d]{5}")){
                messageSystem("PID is not match format, please try again!!!", pidmess);
            }
        }
    }//GEN-LAST:event_IDFocusLost

    private void nameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nameFocusLost
        // TODO add your handling code here:
        if(name.getText().isEmpty()) messageSystem("Name is empty, please check again", namemess);
        else{
            if(!Validate.stringValidationSize(name.getText(),5, 30)){
                messageSystem("Name is not length enough, please try again!!!",namemess);
            }
        }
    }//GEN-LAST:event_nameFocusLost

    private void phonenumFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_phonenumFocusLost
        // TODO add your handling code here:
        if(phonenum.getText().isEmpty()) messageSystem("Phone number is empty, please try again !!!",phonemess);
        else{
            if(!Validate.phoneValidate(phonenum.getText(), 9, 12)){
                messageSystem("Phone num is not right format, please try again !!!", phonemess);
            }
        }
    }//GEN-LAST:event_phonenumFocusLost

